namespace XstarS.ComponentModel
{
    /// <summary>
    /// 表示消息的等级。
    /// </summary>
    public enum MessageLevel
    {
        /// <summary>
        /// 表示调试消息。
        /// </summary>
        Debug = 0,
        /// <summary>
        /// 表示详细消息。
        /// </summary>
        Verbose = 1,
        /// <summary>
        /// 表示信息消息。
        /// </summary>
        Information = 2,
        /// <summary>
        /// 表示警告消息。
        /// </summary>
        Warning = 3,
        /// <summary>
        /// 表示错误消息。
        /// </summary>
        Error = 4
    }
}
